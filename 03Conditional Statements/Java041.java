//Switch 

public class Java041 {
    public static void main(String args[]){
         int number = 2;
         switch(number){
            case 1 : System.out.println("samosa");
            case 2 : System.out.println("burger");     //output
            case 3 : System.out.println("mango shake");    //output
            default : System.out.println("we are dreaming");   //output
            //to avoid this we should use break;
         }
    }
}
